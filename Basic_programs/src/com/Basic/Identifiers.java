package com.Basic;

public class Identifiers {

	public static void main(String[] args) {
		int $number=20;
		System.out.println("value of the variable is:"+$number);
		String studentName="monika";
		System.out.println("value is:"+ studentName);
		String _studentName="monika";
		System.out.println("value is:"+ _studentName);
	}

}
